#Chuyển đổi sang kiểu dữ liệu chuỗi (string)
myString = str("Xin chào")
print(myString)
print(type(myString))

#Chuyển đổi sang kiểu dữ liệu số (integer)
myNumberic = int(8)
print(myNumberic)
print(type(myNumberic))

#Chuyển đổi sang kiểu dữ liệu float
myFloatNumber = float(3)
myFloatNumberTwo = float(3.5)
print(myFloatNumber)
print(myFloatNumberTwo)
print(type(myFloatNumber))
print(type(myFloatNumberTwo))

#Chuyển đổi từ tuple sang list
myTuple = ("Táo","Vàng","Xanh")
print(myTuple)
print(type(myTuple))
myListConver = list(myTuple)
print(myListConver)
print(type(myListConver))

#Chuyển name=value sang dict (json)
myDict = dict(myName="Trong Hoa",myAge=13)
print(myDict)
print(type(myDict))
